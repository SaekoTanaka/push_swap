#include "libft.h"

size_t	ft_count(int n)
{
	size_t	i;

	i = 0;
	if (n < 0)
	{
		i++;
		n = n * -1;
	}
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
