#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	size_t	i;

	i = ft_strlen(s);
	while (*s != '\0')
		s++;
	while (0 < i + 1)
	{
		if (*s == c)
			return ((char*)s);
		s--;
		i--;
	}
	return (NULL);
}
