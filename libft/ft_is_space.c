#include "libft.h"

char	*ft_is_space(const char *s)
{
	while (*s == '\v' || *s == '\n' || *s == '\f'
			|| *s == '\r' || *s == '\t' || *s == ' ')
	{
		s++;
	}
	return ((char*)s);
}
